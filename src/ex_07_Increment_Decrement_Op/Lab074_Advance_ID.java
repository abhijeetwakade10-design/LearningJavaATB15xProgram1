package ex_07_Increment_Decrement_Op;

public class Lab074_Advance_ID {

    public static void main(String[] args){
        int a=10;
        System.out.println(a++ + a);  // 10 +11 =21

        //  A -> a++ ->  A=10, a = 11
        // +
        // B -> a ->  11
        // A+B
        // 10 + 11


        // ERT
        // LNo. |  a  | Exp (print)
        //  6  | 10 | NA
        //  7 |  11 |  21

    }
}
